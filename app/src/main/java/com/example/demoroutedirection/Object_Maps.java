package com.example.demoroutedirection;

public class Object_Maps {
    public double Latitue;
    public double Longtitue;

    public Object_Maps(double Latitue, double Longtitue) {
        this.Latitue = Latitue;
        this.Longtitue = Longtitue;
    }

    public double getLatitue() {
        return Latitue;
    }

    public double getLongtitue() {
        return Longtitue;
    }

    public void setLatitue(double latitue) {
        Latitue = latitue;
    }

    public void setLongtitue(double longtitue) {
        Longtitue = longtitue;
    }
}
